package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int valor1 = 1;
		int valor2 = 2;
		
		//bastante utilizados em estruturas condicionais
		
		System.out.println("Valor1 == Valor2: " + (valor1 == valor2)); //resultado sempre será um valor boolea
		System.out.println("Valor1 != Valor2: " + (valor1 != valor2)); 
		System.out.println("Valor1 > Valor2: " + (valor1 > valor2)); 
		System.out.println("Valor1 >= Valor2: " + (valor1 >= valor2)); 
		System.out.println("Valor1 < Valor2: " + (valor1 < valor2)); 
		System.out.println("Valor1 <= Valor2: " + (valor1 <= valor2));
		

	}

}
