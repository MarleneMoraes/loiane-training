package com.loiane.cursojava.aula20;

public class Matrizes {

	public static void main(String[] args) {
		
		//boas práticas: nomear as variáveis serem no plural quando são matrizes
		double[][] notasAlunos = new double[30][4];
		
		notasAlunos[0][0] = 10;
		notasAlunos[0][1] = 7;
		notasAlunos[0][2] = 9;
		notasAlunos[0][3] = 9.5;
		
		notasAlunos[1][0] = 9;
		notasAlunos[1][1] = 8;
		notasAlunos[1][2] = 7;
		notasAlunos[1][3] = 9;
		
		notasAlunos[2][0] = 9;
		notasAlunos[2][1] = 8;
		notasAlunos[2][2] = 7;
		notasAlunos[2][3] = 9;
		
		
		
	}

}
